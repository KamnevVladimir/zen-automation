    }
    
    /// Генерирует тему в зависимости от категории поста
    static func generateTopic(for category: PostCategory) -> String {
        switch category {
        case .lifehack:
            return randomLifehackTopic()
        case .comparison:
            let dest1 = randomDestination()
            let dest2 = randomDestination()
            return "\(dest1) vs \(dest2)"
        case .budget:
            return "бюджет поездки в \(randomDestination())"
        case .trending:
            return randomSpecificTopic()
        case .destination:
            return randomDestination()
        default:
            // Микс всего
            let options = [
                randomTravelType(),
                randomSpecificTopic(),
                randomSeasonalTopic(),
                "путешествие в \(randomDestination())"
            ]
            return options.randomElement() ?? "путешествия"
        }
    }
    
