                "–ü–æ—á–µ–º—É –≤—Å–µ –±—Ä–æ–Ω–∏—Ä—É—é—Ç –±–∏–ª–µ—Ç—ã –≤ \(topic) –Ω–∞ —Ñ–µ–≤—Ä–∞–ª—å?"
