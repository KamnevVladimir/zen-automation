                "\(topic): —á—Ç–æ –±—É–¥–µ—Ç, –µ—Å–ª–∏ –ø–æ–ª–µ—Ç–µ—Ç—å –≤ –Ω–∏–∑–∫–∏–π —Å–µ–∑–æ–Ω?",
