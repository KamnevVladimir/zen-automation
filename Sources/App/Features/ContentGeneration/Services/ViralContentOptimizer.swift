        case .lifehack:
            variants = [
                "üéØ 5 —Å–ø–æ—Å–æ–±–æ–≤ —Å—ç–∫–æ–Ω–æ–º–∏—Ç—å 50% –Ω–∞ –∞–≤–∏–∞–±–∏–ª–µ—Ç–∞—Ö –≤ 2025",
                "–ü—Ä–æ—Å—Ç–æ–π —Ç—Ä—é–∫ —Å –±–∏–ª–µ—Ç–∞–º–∏, –∫–æ—Ç–æ—Ä—ã–π –∑–Ω–∞—é—Ç —Ç–æ–ª—å–∫–æ –∞–≥–µ–Ω—Ç—ã",
                "–ö–∞–∫ —è –ª–µ—Ç–∞—é –±–∏–∑–Ω–µ—Å-–∫–ª–∞—Å—Å–æ–º –ø–æ —Ü–µ–Ω–µ —ç–∫–æ–Ω–æ–º–∞",
                "–≠—Ç–æ—Ç —Å–µ–∫—Ä–µ—Ç –∞–≤–∏–∞–∫–æ–º–ø–∞–Ω–∏–∏ –Ω–µ —Ö–æ—Ç—è—Ç, —á—Ç–æ–±—ã –≤—ã –∑–Ω–∞–ª–∏",
                "7 –æ—à–∏–±–æ–∫ –ø—Ä–∏ –ø–æ–∫—É–ø–∫–µ –±–∏–ª–µ—Ç–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ —Å—Ç–æ—è—Ç –≤–∞–º –¥–µ–Ω–µ–≥"
