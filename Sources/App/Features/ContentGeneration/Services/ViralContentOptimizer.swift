    /// –û–ø—Ç–∏–º–∏–∑–∞—Ü–∏—è —Ç–µ–≥–æ–≤ –¥–ª—è –î–∑–µ–Ω–∞ (SEO)
    func optimizeTags(for topic: String, category: PostCategory) -> [String] {
        var tags: [String] = []
        
        // –ë–∞–∑–æ–≤—ã–µ —Ç–µ–≥–∏
        tags.append("–ø—É—Ç–µ—à–µ—Å—Ç–≤–∏—è")
