            "\n\n‚ù§Ô∏è –õ–∞–π–∫, –µ—Å–ª–∏ —Å—Ç–∞—Ç—å—è –±—ã–ª–∞ –ø–æ–ª–µ–∑–Ω–æ–π! –í–∞—à–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∞ –º–æ—Ç–∏–≤–∏—Ä—É–µ—Ç –º–µ–Ω—è –¥–µ–ª–∏—Ç—å—Å—è –µ—â—ë –±–æ–ª—å—à–µ —Å–µ–∫—Ä–µ—Ç–æ–≤ —ç–∫–æ–Ω–æ–º–∏–∏.",
