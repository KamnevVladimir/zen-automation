                "–¢—É—Ä—Ü–∏—è VS –ï–≥–∏–ø–µ—Ç 2025: –≥–¥–µ –¥–µ—à–µ–≤–ª–µ –∏ –ª—É—á—à–µ?",
                "–Ø —Å—ä–µ–∑–¥–∏–ª –≤ –æ–±–∞ –º–µ—Å—Ç–∞ - –≤–æ—Ç –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω—ã–π –≤–µ—Ä–¥–∏–∫—Ç",
                "\(topic): —á—Ç–æ –≤—ã–±–∏—Ä–∞—é—Ç 90% –ø—É—Ç–µ—à–µ—Å—Ç–≤–µ–Ω–Ω–∏–∫–æ–≤?",
                "–®–æ–∫–∏—Ä—É—é—â–∞—è –ø—Ä–∞–≤–¥–∞ –æ \(topic) - –º–æ–π –ª–∏—á–Ω—ã–π –æ–ø—ã—Ç"
            ]
            
        case .budget:
            variants = [
                "üí∞ –û—Ç–ø—É—Å–∫ –∑–∞ 30–∫‚ÇΩ: 5 —Å—Ç—Ä–∞–Ω, –≥–¥–µ —ç—Ç–æ —Ä–µ–∞–ª—å–Ω–æ –≤ 2025",
                "–ö–∞–∫ —è —Å–ª–µ—Ç–∞–ª –Ω–∞ –º–æ—Ä–µ —Å —Å–µ–º—å—ë–π –∑–∞ 50 —Ç—ã—Å—è—á —Ä—É–±–ª–µ–π",
                "–ë—é–¥–∂–µ—Ç–Ω—ã–π –æ—Ç–¥—ã—Ö: –∫—É–¥–∞ –ø–æ–µ—Ö–∞—Ç—å, –µ—Å–ª–∏ –¥–µ–Ω–µ–≥ –º–∞–ª–æ",
                "7 –Ω–µ–¥–æ–æ—Ü–µ–Ω—ë–Ω–Ω—ã—Ö –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–π –¥–µ—à–µ–≤–ª–µ –¢—É—Ä—Ü–∏–∏",
                "–≠—Ç–æ—Ç —Å–ø–æ—Å–æ–± —Å—ç–∫–æ–Ω–æ–º–∏–ª –º–Ω–µ 40% –Ω–∞ –æ—Ç–ø—É—Å–∫–µ"
